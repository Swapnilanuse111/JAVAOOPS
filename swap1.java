//USING THIRD VARIBALE SWAPPING TWO NUMBERS 

public class swap1 {
    public static void main (String[]args)
    {
        int a=20,b=30;
        System.out.println("Befor Swapping Two Number"+a+" "+b);

        int t=b;//a=20,t=a mens t=20
        b=a; //b=20
        a=t;    //a=3=
        System.out.println("After Swapping two number"+a+" "+b);
    }
}
