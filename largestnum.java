public class largestnum {
    public static void main(String[]args)
    {
        int a[]={20,30,20,100,30,70};
        int max=a[0];
        for(int i=1;i<a.length;i++)
        {
            if(max<a[i])
            {
                max=a[i];
            }
        }
        System.out.println("largest number is:\n"+max);
    }
}
